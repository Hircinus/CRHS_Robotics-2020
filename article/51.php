The first and one of the most important steps in our process is deciding on what theme we would choose for our team. It
affects most aspects of our work and we took this step very seriously. We had a few ideas on potential themes:
TSN/SportsCentre, Night at the Museum, the Renaissance, etc. Eventually we narrowed it down to two: TSN and the
Renaissance. From there we had two teams make a presentation for their theme of choice and present it to the rest of the
robotics team, where we would vote on which theme we wanted to go with.
<br>
<br>
Initially, there was an interesting split between the grades. The sec. 5 members were supportive of the TSN theme while
the sec. 4 students wanted to go with the Renaissance. After the presentation, it was clear who the winner was: TSN! The
deciding factor was how difficult it would be to differentiate between the Renaissance and the Middle Ages. The TSN team
also created a more in-depth plan of how our theme would influence the parts of robotics that would be affected by the
theme.
<br>
<br>
In the end, we have chosen TSN and are working hard on incorporating it into our kiosk, video, etc. We hope our
dedication to the theme shows and that you enjoy how we have incorporated it into our work.
